// Show the mobile navigation menu when you click on the icon
// Contributer: Kali
function showMobileNav() {
    var x = document.getElementById("responsiveMenu");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
    }
}




